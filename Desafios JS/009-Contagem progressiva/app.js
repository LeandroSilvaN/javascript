//Crie um programa de contagem progressiva. Peça um número e conte de 0 até esse número, usando um loop while no console do navegador.

let numero = prompt('Diga um número:');
let contagem = 0;

while (numero != contagem) {
    alert(contagem);
    contagem++;
    if (numero == contagem) {
        alert(numero);
    }
}